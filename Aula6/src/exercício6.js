// Exercício 6: Escreva um algoritmo para ler as dimensões de um retãngulo (base e altura), calcular e escrever a área do retãngulo.

alert("Área de Retãngulo");

let base = parseFloat(prompt("Informe o valor da base do retãngulo: "));
let altura = parseFloat(prompt("Informe o valor da altura do retãngulo: "));

let areaRetangulo = (base * altura);

alert(`O valor da área do retãngulo de base ${base} e altura ${altura} é: ${areaRetangulo}`);

